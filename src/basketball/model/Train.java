package basketball.model;

public class Train {
     private String shuttlerun;
     private String benchpress;
     private String fmeters;
     private String ometers;
     private String setshot;
     private String addtime;
	public Train() {
		super();
	}
	public String getShuttlerun() {
		return shuttlerun;
	}
	public void setShuttlerun(String shuttlerun) {
		this.shuttlerun = shuttlerun;
	}
	public String getBenchpress() {
		return benchpress;
	}
	public void setBenchpress(String benchpress) {
		this.benchpress = benchpress;
	}
	public String getFmeters() {
		return fmeters;
	}
	public void setFmeters(String fmeters) {
		this.fmeters = fmeters;
	}
	public String getOmeters() {
		return ometers;
	}
	public void setOmeters(String ometers) {
		this.ometers = ometers;
	}
	public String getSetshot() {
		return setshot;
	}
	public void setSetshot(String setshot) {
		this.setshot = setshot;
	}
	public String getAddtime() {
		return addtime;
	}
	public void setAddtime(String addtime) {
		this.addtime = addtime;
	}
     
}
